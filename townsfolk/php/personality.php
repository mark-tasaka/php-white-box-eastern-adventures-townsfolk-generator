            {
                $personality = getPersonalityPositiveTwo() . ', ' . getPersonalityPositiveOne()
                    . ' and ' . getPersonalityNeutralTwo();
            }
            else if($number === 6)
            {
                $personality = getPersonalityPositiveTwo() . ', ' . getPersonalityNeutralOne()
                    . ' and ' . getPersonalityNeutralTwo();
            }
            else if($number === 7)
            {
                $personality = getPersonalityNegativeTwo() . ', ' . getPersonalityNeutralOne()
                    . ' and ' . getPersonalityNeutralTwo();
            }
            else if($number === 8)
            {
                $personality = getPersonalityNegativeTwo() . ', ' . getPersonalityNeutralOne() 
                    . ' and ' . getPersonalityNegativeThree();
            }
            else if($number === 9)
            {
                $personality = getPersonalityNegativeOne() . ', ' . getPersonalityNeutralThree() . ', ' 
                    . getPersonalityNegativeTwo() . ' and ' . getPersonalityNeutralOne();
            }
