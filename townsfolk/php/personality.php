            {
                $personality = getPersonalityNeutralTwo() . ', ' . getPersonalityPositiveOne() . ', ' 
                    . getPersonalityPositiveFour() . ', ' . getPersonalityPositiveTwo() . ' and ' . getPersonalityNeutralOne();
            }
